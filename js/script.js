let word = prompt('ჩაწერე სიტყვა ინგლისურად(ვნახოთ თუ ხმოვანზე იწყება)');

if (
  word[0] === 'a' ||
  word[0] === 'A' ||
  word[0] === 'e' ||
  word[0] === 'E' ||
  word[0] === 'i' ||
  word[0] === 'I' ||
  word[0] === 'o' ||
  word[0] === 'O' ||
  word[0] === 'u' ||
  word[0] === 'U'
) {
  alert(`სიტყვა იწყება ხმოვანით, რომელიც არის ${word[0]}`);
} else {
  alert(`სიტყვა არ იწყება ხმოვანით, რადგან ის არის ${word[0]}`);
}
